    /**
     * <p>
     * Opens the Clienti window.
     */
    private void clientButtonClicked(){
        if( FrameInstanceHolder.instance.getClientsFrame() == null ){
            ClientsGUI clientsFrame = new ClientsGUI();
            FrameInstanceHolder.instance.setClientsFrame( clientsFrame );
            clientsFrame.setVisible( true );
        }else{
            FrameInstanceHolder.instance.getClientsFrame().toFront();
        }
    }
    
