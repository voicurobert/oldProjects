    /**
     * <p>
     * Opens the Agent Furnizor window.
     */
    private void providerButtonClicked(){
        if( FrameInstanceHolder.instance.getProviderFrame()== null ){
            ProviderGUI providerFrame = new ProviderGUI();
            FrameInstanceHolder.instance.setProviderFrame(providerFrame);
            providerFrame.setVisible( true );
        }else{
            FrameInstanceHolder.instance.getProviderFrame().toFront();
        }
    }
    
    /**
     * <p>
     * Reseting all data from DB.
     */
    private void resetDataButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_resetDataButtonActionPerformed
        // TODO add your handling code here:
        if( DatabaseManager.instance.isConnected() ){
            try{
                DatabaseManager.instance.statement.executeUpdate( "DELETE FROM oferte" );
                DatabaseManager.instance.statement.executeUpdate( "DELETE FROM clienti" );
                DatabaseManager.instance.statement.executeUpdate( "DELETE FROM centroizi" );
                DatabaseManager.instance.statement.executeUpdate( "ALTER TABLE clienti AUTO_INCREMENT = 1" );
                DatabaseManager.instance.statement.executeUpdate( "ALTER TABLE oferte AUTO_INCREMENT = 1" );
                DatabaseManager.instance.statement.executeUpdate( "ALTER TABLE centroizi AUTO_INCREMENT = 1" );
                
            }catch( SQLException e){
                System.out.println(e.getMessage() );
            }
        }
    }//GEN-LAST:event_resetDataButtonActionPerformed

